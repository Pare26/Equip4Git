        public async Task<Response> CheckConnection()
        {
            //verificar que tingui internet connectat al mobil del pluguin connectivity
            if (!CrossConnectivity.Current.IsConnected)
            {
                return new Response
                {
                    IsSuccess = false,
                    Message = "Please turn on your internet settings.",
                };
            }
            //connectar-se a una web per verificar que pot accedir-hi
            var isReachable = await CrossConnectivity.Current.IsRemoteReachable(
                "google.com");
            if (!isReachable)
            {
                return new Response
                {
                    IsSuccess = false,
                    Message = "Check you internet connection.",
                };
            }

            return new Response
            {
                IsSuccess = true,
                Message = "Ok",
            };
        }

        public async Task<Usuario> ConsultarUsuario(string email, string password)
        {
            var conexion = $"http://192.168.0.10/WebService/GetUsuario.php?email=" + email + "&password=" + password;

            using (var cliente = new HttpClient())
            {
                var peticion = await cliente.GetAsync(conexion);

                if (peticion != null)
                {
                    var json = await peticion.Content.ReadAsStringAsync();

                    var datos = (JContainer)JsonConvert.DeserializeObject(json);

                    if (datos["Usuari"] != null)
                    {
                        var usuario = new Usuario();

                        usuario.ID_Usuari = (int)datos["Usuari"]["ID_Usuari"];
                        usuario.Nom = (string)datos["Usuari"]["Nom"];
                        usuario.Cognom = (string)datos["Usuari"]["Cognom"];
                        usuario.Email = (string)datos["Usuari"]["Email"];
                        usuario.Password = (string)datos["Usuari"]["Password"];

                        return usuario;
                    }
                }
            }
            return default(Usuario);
        }

        public async Task<Response> GetList<T>(
            string urlBase,
            string servicePrefix,
            string controller)
        {
            try
            {
                var client = new HttpClient();
                client.BaseAddress = new Uri(urlBase);
                var url = string.Format("{0}{1}", servicePrefix, controller);
                var response = await client.GetAsync(url);
                var result = await response.Content.ReadAsStringAsync();

                if (!response.IsSuccessStatusCode)
                {
                    return new Response
                    {
                        IsSuccess = false,
                        Message = result,
                    };
                }

                var list = JsonConvert.DeserializeObject<List<T>>(result);
                return new Response
                {
                    IsSuccess = true,
                    Message = "Ok",
                    Result = list,
                };
            }
            catch (Exception ex)
            {
                return new Response
                {
                    IsSuccess = false,
                    Message = ex.Message,
                };
            }
        }
        public async Task<Response> Post<T>(
            string urlBase,
            string servicePrefix,
            string controller,
            string tokenType,
            string accessToken,
            T model)
        {
            try
            {
                var request = JsonConvert.SerializeObject(model);
                var content = new StringContent(
                    request, Encoding.UTF8,
                    "application/json");
                var client = new HttpClient();
                client.DefaultRequestHeaders.Authorization =
                    new AuthenticationHeaderValue(tokenType, accessToken);
                client.BaseAddress = new Uri(urlBase);
                var url = string.Format("{0}{1}", servicePrefix, controller);
                var response = await client.PostAsync(url, content);
                var result = await response.Content.ReadAsStringAsync();

                if (!response.IsSuccessStatusCode)
                {
                    var error = JsonConvert.DeserializeObject<Response>(result);
                    error.IsSuccess = false;
                    return error;
                }

                var newRecord = JsonConvert.DeserializeObject<T>(result);

                return new Response
                {
                    IsSuccess = true,
                    Message = "Record added OK",
                    Result = newRecord,
                };
            }
            catch (Exception ex)
            {
                return new Response
                {
                    IsSuccess = false,
                    Message = ex.Message,
                };
            }
        }

